    private void addButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButton1ActionPerformed
        // TODO add your handling code here:
        ConnectLib.doConnect();
        if (    jTextName.getText().equals("")
            || jTextID.getText().equals("")
            || jTextPass.getText().equals("")
            || jTextUserName.getText().equals("")
            || jTextAdress.getText().equals("")
            || jTextEmail.getText().equals("")
            || jTextSalary.getText().equals("")
            || jDateBirth.getDateFormatString().equals("")
            || jTextPhone.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn Phải Nhập Đầy Đủ Thông tin");
        } else {

            Librarian Lib = new Librarian();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(jDateBirth.getDate());
            Lib.setID(jTextID.getText());
            Lib.setName(jTextName.getText());
            Lib.setBirth((Date.valueOf(date)));
            Lib.setGender(jComboBoxGT.getSelectedItem().toString());
            Lib.setAddress(jTextAdress.getText());
            Lib.setEmail(jTextEmail.getText());
            Lib.setPhone(jTextPhone.getText());
            Lib.setSalary(Integer.parseInt(jTextSalary.getText()));
            Lib.setUsername(jTextUserName.getText());
            Lib.setPassword(jTextPass.getText());
            LibController.addlib(Lib, jTable1);
        }
    }//GEN-LAST:event_addButton1ActionPerformed

