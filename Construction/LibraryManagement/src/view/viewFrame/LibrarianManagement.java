    private void undoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoButtonActionPerformed
        // TODO add your handling code here:
        ConnectLib.doConnect();
        if (stacklib.empty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Xóa Hàng Nào Nên Không Thể Undo");
        } else {
            Librarian lib = (Librarian) stacklib.pop();
            ConnectLib.insertLibrarian(lib);
        }
        ((DefaultTableModel) jTable3.getModel()).setNumRows(0);

            LibController.Loaddatatotable(ConnectLib.getAllLibrarians(), jTable3);
    }//GEN-LAST:event_undoButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable3.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Hãy Chọn Một Hàng Trong Bảng Để Xoá ?");
        } else {
            ConnectLib.doConnect();
            Librarian lib = LibController.getLibrarianFromTable(jTable3, row);
            stacklib.add(lib);
            String MaNV = (String) jTable3.getModel().getValueAt(row, 0);
            ConnectLib.deleteLibrarian(MaNV);
            LibController.Loaddatatotable(ConnectLib.getAllLibrarians(), jTable3);        
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

