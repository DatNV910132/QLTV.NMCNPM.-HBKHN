    private void addButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButton1ActionPerformed
        int billRow = jTable1.getSelectedRow();
        if (billRow < 0) {
            JOptionPane.showMessageDialog(null, "Bạn phải chọn hóa đơn trước!");
        } else {
            connectBillDetail.doConnect();
            if (jTextBookID.getText().equals("")
                    || jTextNumber.getText().equals("")
                    || jTextDeposit.getText().equals("")
                    || jDateBorrowDay.getDateFormatString().equals("")
                    || jDatePayDay.getDateFormatString().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn Phải Nhập Đầy Đủ Thông tin");
            } else {
                BillDetail billDetail = new BillDetail();
                billDetail.setBillID(jTable1.getValueAt(billRow, 0).toString());
                billDetail.setBookID(jTextBookID.getText());
                billDetail.setDeposit(Integer.valueOf(jTextDeposit.getText()));
                billDetail.setNumber(Integer.valueOf(jTextNumber.getText()));
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(jDateBorrowDay.getDate());
                billDetail.setBorrowingDay(Date.valueOf(date));
                date = sdf.format(jDatePayDay.getDate());
                billDetail.setPayDay(Date.valueOf(date));
                billDetailController.addBillDetail(billDetail, jTable5);
            }
        }
    }//GEN-LAST:event_addButton1ActionPerformed

