    private void btnSaveBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveBillActionPerformed
        int row = jTable4.getSelectedRow();
        connectBill.doConnect();
        if (jTextBillID1.getText().equals("")
                || jTextReaderID1.getText().equals("")
                || jTextLibararianID1.getText().equals("")
                || jTextStatus1.getText().equals("")
                || jTextTotalDeposit1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn Phải Nhập Đầy Đủ Thông tin");
        } else {
            Bill bill = new Bill();
            bill.setLibrarianID(jTextLibararianID1.getText());
            bill.setReaderID(jTextReaderID1.getText());
            bill.setStatus(jTextStatus1.getText());
            bill.setTotalMoney(Integer.valueOf(jTextTotalDeposit1.getText()));
            connectBill.updateBill(String.valueOf(jTable4.getModel().getValueAt(row, 0)), bill);
            ((DefaultTableModel) jTable4.getModel()).setNumRows(0);

            billController.Loaddatatotable(connectBill.getAllBills(), jTable4);
        }
    }//GEN-LAST:event_btnSaveBillActionPerformed

