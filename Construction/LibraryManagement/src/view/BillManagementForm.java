    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable7.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Hãy Chọn Một Hàng Trong Bảng Để Xoá ?");
        } else {
            connectBill.doConnect();
            connectBillDetail.doConnect();
            Bill bill = billController.getBillFromTable(jTable7, row);
            stackBills.add(bill);
            String ID = (String) jTable7.getModel().getValueAt(row, 0);
            connectBillDetail.DeleteBillDetailByBillID(ID);
            connectBill.deleteBill(ID);
            billController.Loaddatatotable(connectBill.getAllBills(), jTable7);
            billDetailController.Loaddatatotable(connectBillDetail.getBillDetailByBillID(ID), jTable8);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void undoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoButtonActionPerformed
        // TODO add your handling code here:
        connectBill.doConnect();
        if (stackBills.empty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Xóa Hàng Nào Nên Không Thể Undo");
        } else {
            Bill bill = (Bill) stackBills.pop();
            connectBill.insertBill(bill);
        }
        ((DefaultTableModel) jTable7.getModel()).setNumRows(0);

        billController.Loaddatatotable(connectBill.getAllBills(), jTable7);
    }//GEN-LAST:event_undoButtonActionPerformed

