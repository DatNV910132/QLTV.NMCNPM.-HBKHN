    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Hãy Chọn Một Hàng Trong Bảng Để Sửa?");
        } else {
            jTextID1.setText((String) jTable2.getModel().getValueAt(row, 0));
            jTextName1.setText((String) jTable2.getModel().getValueAt(row, 1));
            jDateBirth1.setDate((Date) jTable2.getModel().getValueAt(row, 2));
            jComboBoxGT1.setSelectedItem((String) jTable2.getModel().getValueAt(row, 3));
            jTextAdress1.setText((String) jTable2.getModel().getValueAt(row, 4));
            jTextPhone1.setText((String) jTable2.getModel().getValueAt(row, 5));
            jTextEmail1.setText((String) jTable2.getModel().getValueAt(row, 6));
        }
    }//GEN-LAST:event_updateButtonActionPerformed

