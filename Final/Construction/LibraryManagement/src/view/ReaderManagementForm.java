    private void addButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButton1ActionPerformed
        // TODO add your handling code here:
        connectReader.doConnect();
        if (    jTextName.getText().equals("")
            || jTextID.getText().equals("")
            || jTextAdress.getText().equals("")
            || jTextEmail.getText().equals("")
            || jDateBirth.getDateFormatString().equals("")
            || jTextPhone.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn Phải Nhập Đầy Đủ Thông tin");
        } else {

            Reader reader = new Reader();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(jDateBirth.getDate());
            reader.setID(jTextID.getText());
            reader.setName(jTextName.getText());
            reader.setBirth((Date.valueOf(date)));
            reader.setGender(jComboBoxGT.getSelectedItem().toString());
            reader.setAddress(jTextAdress.getText());
            reader.setEmail(jTextEmail.getText());
            reader.setPhone(jTextPhone.getText());
            readerController.addReader(reader, jTable1);
        }
    }//GEN-LAST:event_addButton1ActionPerformed

