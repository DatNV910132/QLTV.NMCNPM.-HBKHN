    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable3.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Hãy Chọn Một Hàng Trong Bảng Để Xoá ?");
        } else {
            connectReader.doConnect();
            Reader reader = readerController.getReaderFromTable(jTable3, row);
            stackReader.add(reader);
            String MaDG = (String) jTable3.getModel().getValueAt(row, 0);
            connectReader.deleteReader(MaDG);
            readerController.Loaddatatotable(connectReader.getAllReaders(), jTable3);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void undoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoButtonActionPerformed
        // TODO add your handling code here:
        connectReader.doConnect();
        if (stackReader.empty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Xóa Hàng Nào Nên Không Thể Undo");
        } else {
            Reader reader = (Reader) stackReader.pop();
            connectReader.insertReader(reader);
        }
        ((DefaultTableModel) jTable3.getModel()).setNumRows(0);

        readerController.Loaddatatotable(connectReader.getAllReaders(), jTable3);
    }//GEN-LAST:event_undoButtonActionPerformed

